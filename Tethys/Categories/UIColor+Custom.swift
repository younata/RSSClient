import UIKit

extension UIColor {
    static var darkGreen: UIColor {
        return UIColor(red: 0, green: 0.75, blue: 0, alpha: 1.0)
    }

    static var mediumDarkGray: UIColor {
        return UIColor(white: 0.4, alpha: 1.0)
    }

    static var darkViolet: UIColor {
        return UIColor(red: 0.25, green: 0, blue: 0.5, alpha: 1.0)
    }
}
